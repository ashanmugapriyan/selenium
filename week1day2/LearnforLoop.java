package week1day2;

public class LearnforLoop {

	public static void main(String[] args) {
		
		
		for (int n=1;n<=10;n++)
		{
			System.out.println(n);
			
			
		}		
		System.out.println("To print the reverse order");
		for (int r=10;r>0;r--)
		{
			System.out.println(r);
		}
	}

}
